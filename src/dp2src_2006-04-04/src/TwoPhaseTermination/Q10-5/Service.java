public class Service {
    // サービス実行開始
    public static void service() {
        System.out.print("service");
        for (int i = 0; i < 50; i++) {
            System.out.print(".");
            try {
                Thread.sleep(100);
            } catch (InterruptedException e) {
            }
        }
        System.out.println("done.");
    }

    // サービス中止
    public static void cancel() {
        // 未実装
    }
}
